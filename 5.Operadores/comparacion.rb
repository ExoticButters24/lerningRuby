#Mayor que
puts 10 > 5

#Menor que
puts 5 < 10

#Igualdad (funciona con cualquier objeto y con valores incluso si el tipo de dato es diferente)
puts 10 == 10
puts "si" == "si"
puts 1 == 1.0

#Mayor o igual que
puts 5 >= 5

#Menor o igual que
puts 5 <= 5

#Diferencia
puts 4 != 6

#Comparación entre 2 objetos / Comparación combinado
puts 10<=>10 #Salida 0 (si son iguales)
puts 20<=>10 #Salida 1 (si el primero es mayor que el segundo)
puts 10<=>20 #Salida -1 (si el segundo es mayor que el primero)